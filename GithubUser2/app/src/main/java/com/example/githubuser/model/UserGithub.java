package com.example.githubuser.model;

public class UserGithub{
    private String avtr, user, name, loca, repo, comp, flwr, flng;

    public String getAvtr() {
        return avtr;
    }

    public void setAvtr(String avtr) {
        this.avtr = avtr;
    }

    public String getUser() {
        return user;
    }

    public void setUser(String user) {
        this.user = user;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getLoca() {
        return loca;
    }

    public void setLoca(String loca) {
        this.loca = loca;
    }

    public String getRepo() {
        return repo;
    }

    public void setRepo(String repo) {
        this.repo = repo;
    }

    public String getComp() {
        return comp;
    }

    public void setComp(String comp) {
        this.comp = comp;
    }

    public String getFlwr() {
        return flwr;
    }

    public void setFlwr(String flwr) {
        this.flwr = flwr;
    }

    public String getFlng() {
        return flng;
    }

    public void setFlng(String flng) {
        this.flng = flng;
    }
}
